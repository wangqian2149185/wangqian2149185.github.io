function setTheme(theme) {
  const nextTheme = theme === "dark" ? "dark" : "light";
  document.documentElement.dataset.theme = nextTheme;
  localStorage.setItem("portfolio-theme", nextTheme);

  const button = document.querySelector("[data-theme-toggle]");
  if (button) {
    const isDark = nextTheme === "dark";
    button.textContent = isDark ? "☀" : "☾";
    button.setAttribute("aria-label", isDark ? "Switch to light mode" : "Switch to dark mode");
    button.setAttribute("title", isDark ? "Switch to light mode" : "Switch to dark mode");
  }
}

function setLanguage(lang) {
  const dictionary = translations[lang] || translations.en;
  document.documentElement.lang = lang === "zh" ? "zh-CN" : "en";
  localStorage.setItem("portfolio-lang", lang);

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.getAttribute("data-i18n");
    if (dictionary[key]) {
      node.textContent = dictionary[key];
    }
  });

  document.querySelectorAll(".lang-button").forEach((button) => {
    button.classList.toggle("active", button.dataset.lang === lang);
  });

  startQuoteRotator(lang);
}

function formatQuote(text) {
  return `“${text}”`;
}

function chooseQuote(lang) {
  const quotes = quoteLibrary[lang] || quoteLibrary.en;
  if (quotes.length <= 1) {
    quoteState.index = 0;
    return quotes[0] || "";
  }

  let nextIndex = Math.floor(Math.random() * quotes.length);
  while (nextIndex === quoteState.index) {
    nextIndex = Math.floor(Math.random() * quotes.length);
  }
  quoteState.index = nextIndex;
  return quotes[nextIndex];
}

function startQuoteRotator(lang) {
  const node = document.querySelector(".hero-copy");
  if (!node) {
    return;
  }

  quoteState.token += 1;
  const token = quoteState.token;
  window.clearTimeout(quoteState.timeout);

  const quote = formatQuote(chooseQuote(lang));
  let position = 0;
  node.textContent = "";

  function typeNextCharacter() {
    if (token !== quoteState.token) {
      return;
    }

    node.textContent = quote.slice(0, position);
    position += 1;

    if (position <= quote.length) {
      quoteState.timeout = window.setTimeout(typeNextCharacter, 34);
      return;
    }

    quoteState.timeout = window.setTimeout(() => startQuoteRotator(lang), 8000);
  }

  typeNextCharacter();
}

function getVisitorId() {
  const storageKey = "portfolio-visitor-id";
  const existing = localStorage.getItem(storageKey);
  if (existing) {
    return existing;
  }

  const randomPart =
    window.crypto?.randomUUID?.() ||
    `${Date.now().toString(36)}-${Math.random().toString(36).slice(2)}`;
  localStorage.setItem(storageKey, randomPart);
  return randomPart;
}

function logVisit() {
  if (!visitLoggerConfig.endpoint) {
    return;
  }

  const payload = {
    token: visitLoggerConfig.token,
    visitorId: getVisitorId(),
    url: window.location.href,
    path: `${window.location.pathname}${window.location.search}${window.location.hash}`,
    referrer: document.referrer,
    language: navigator.language,
    timezone: Intl.DateTimeFormat().resolvedOptions().timeZone,
    screen: `${window.screen.width}x${window.screen.height}`,
    userAgent: navigator.userAgent,
  };

  const body = JSON.stringify(payload);

  if (navigator.sendBeacon) {
    const sent = navigator.sendBeacon(
      visitLoggerConfig.endpoint,
      new Blob([body], { type: "text/plain;charset=utf-8" }),
    );
    if (sent) {
      return;
    }
  }

  fetch(visitLoggerConfig.endpoint, {
    method: "POST",
    mode: "no-cors",
    keepalive: true,
    headers: {
      "Content-Type": "text/plain;charset=utf-8",
    },
    body,
  }).catch(() => {
    // Analytics should never affect the portfolio experience.
  });
}

document.addEventListener("DOMContentLoaded", () => {
  const saved = localStorage.getItem("portfolio-lang");
  const preferred = navigator.language.toLowerCase().startsWith("zh") ? "zh" : "en";
  const activeTheme = document.documentElement.dataset.theme || "light";
  setTheme(activeTheme);
  setLanguage(saved || preferred);

  document.querySelectorAll(".lang-button").forEach((button) => {
    button.addEventListener("click", () => setLanguage(button.dataset.lang));
  });

  document.querySelector("[data-theme-toggle]")?.addEventListener("click", () => {
    const current = document.documentElement.dataset.theme === "dark" ? "dark" : "light";
    setTheme(current === "dark" ? "light" : "dark");
  });

  logVisit();
});
